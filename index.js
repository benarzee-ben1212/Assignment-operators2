// += operator
num1 += 11 //adds 11 to the above 10 from line 6 num1 =num1+11
console.log(num1)

// // -= operator
num1 -= 5 //subtracts 5 from the above 11 from line 10 num1=num1-5
console.log(num1)

//*= operator
num1 *= 5
console.log(num1) //num1 = num1*5

// /= operator
num1 /= 5
console.log(num1) //num1 = num1/5

//%= operator
num1 %=5
console.log(num1) //num1=num1%5 (remainder)

